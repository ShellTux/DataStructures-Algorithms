#define HANDLE_ERROR(MSG)                               \
    do {                                                \
        fprintf(stderr, "%s:%d: ", __FILE__, __LINE__); \
        perror(MSG);                                    \
        exit(EXIT_FAILURE);                             \
    } while (0);

